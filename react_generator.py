"""
React + Three.js component generation.

Converts L-system / IFS geometry into a self-contained React component
(.jsx) and an HTML iframe preview (Canvas 2D) for the Streamlit embed.

Constants:
    MAX_REACT_PAYLOAD_BYTES  — hard cap on serialized JSON size.
    REACT_COMPONENT_TEMPLATE — stable JSX skeleton injected with params.

Functions:
    extract_lsystem_geometry  — normalise segments for Three.js.
    extract_ifs_geometry      — normalise IFS point cloud for Three.js.
    build_threejs_params      — serialise geometry + config to JS literal.
    generate_react_component  — slot params into the JSX template.
    generate_iframe_html      — wrap for live Streamlit Canvas 2D preview.
"""

import json

import streamlit as st


