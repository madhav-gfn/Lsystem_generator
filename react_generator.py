def build_threejs_params(geometry_dict, colors, animation_speed=1.0,
                         parallax_strength=0.4, particle_size=0.008,
                         glow_layers=2, bg_color="#0a0a0f", label=""):
    """Merge geometry data with UI config into a JavaScript object literal.

    Args:
        geometry_dict: Dict from extract_lsystem_geometry or extract_ifs_geometry.
        colors: List of hex colour strings.
        animation_speed: Float 0.1–2.0.
        parallax_strength: Float 0.0–1.0.
        particle_size: Float for THREE.PointsMaterial size.
        glow_layers: Integer 1–3.
        bg_color: Hex string for page background.
        label: Human-readable label for generated code comment.

    Returns:
        A string containing ``const INJECTED_PARAMS = { ... };``.
    """
    payload = {
        "type": geometry_dict["type"],
        "colors": colors,
        "animationSpeed": animation_speed,
        "parallaxStrength": parallax_strength,
        "particleSize": particle_size,
        "lineWidth": 1,
        "glowLayers": glow_layers,
        "backgroundColor": bg_color,
        "label": label,
    }

    if geometry_dict["type"] == "lsystem":
        payload["vertices"] = geometry_dict["vertices"]
        payload["edges"] = geometry_dict["edges"]
    else:
        payload["points"] = geometry_dict["points"]

    serialized = json.dumps(payload, separators=(",", ":"))

    # Enforce payload size limit
    if len(serialized.encode("utf-8")) > MAX_REACT_PAYLOAD_BYTES:
        # Truncate geometry arrays
        if "edges" in payload:
            keep = max(100, len(payload["edges"]) // 2)
            payload["edges"] = payload["edges"][:keep]
            payload["vertices"] = payload["vertices"][:keep]
        elif "points" in payload:
            keep = max(100, len(payload["points"]) // 2)
            payload["points"] = payload["points"][:keep]
        serialized = json.dumps(payload, separators=(",", ":"))
        st.warning(
            f"React payload exceeded {MAX_REACT_PAYLOAD_BYTES:,} bytes — "
            f"geometry was truncated to fit."
        )

    return f"const INJECTED_PARAMS = {serialized};"


def generate_react_component(injected_params_js, label=""):
    """Slot INJECTED_PARAMS into the stable React template.

    Args:
        injected_params_js: JS string from build_threejs_params().
        label: Human-readable label for the comment header.

    Returns:
        Complete JSX file contents as a Python string.
    """
    src = REACT_COMPONENT_TEMPLATE.replace(
        "{INJECTED_PARAMS_PLACEHOLDER}", injected_params_js
    )
    src = src.replace("{LABEL_COMMENT}", label)
    return src


def generate_iframe_html(react_source, bg_color="#0a0a0f"):
    """Build a self-contained HTML preview using Canvas 2D.

    This preview does NOT load any external scripts (React, Three.js, Babel).
    Instead it uses the browser's built-in Canvas 2D API to render the
    geometry with animation, glow, and mouse parallax — all inline.

    The downloadable .jsx file (react_source) still contains the full
    React + Three.js component.  This function only reads the
    INJECTED_PARAMS from that source to build the preview.

    Args:
        react_source: Complete JSX source (used to extract INJECTED_PARAMS).
        bg_color: Hex background colour for the page body.

    Returns:
        Complete HTML string ready for ``st.components.v1.html()``.
    """
    # Extract the INJECTED_PARAMS line from the react source
    params_line = ""
    for line in react_source.split("\n"):
        stripped = line.strip()
        if stripped.startswith("const INJECTED_PARAMS"):
            params_line = stripped
            break

    html = f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8"/>
<meta name="viewport" content="width=device-width,initial-scale=1"/>
<style>
*{{margin:0;padding:0;box-sizing:border-box}}
html,body,canvas{{width:100%;height:100%;overflow:hidden;background:{bg_color};display:block}}
</style>
</head>
<body>
<canvas id="c"></canvas>
<script>
// --- Injected geometry and config ---
{params_line}
const P = INJECTED_PARAMS;

// --- Canvas setup ---
const canvas = document.getElementById('c');
const ctx = canvas.getContext('2d');
let W, H;
function resize() {{
  W = canvas.width = window.innerWidth;
  H = canvas.height = window.innerHeight;
}}
resize();
window.addEventListener('resize', resize);

// --- Mouse tracking ---
let mx = 0, my = 0;
window.addEventListener('mousemove', e => {{
  mx = (e.clientX / W) * 2 - 1;
  my = (e.clientY / H) * 2 - 1;
}});

// --- Parse hex color ---
function hexToRgb(hex) {{
  const r = parseInt(hex.slice(1,3),16);
  const g = parseInt(hex.slice(3,5),16);
  const b = parseInt(hex.slice(5,7),16);
  return [r, g, b];
}}

// --- Animation state ---
let angle = 0;
let parallaxX = 0, parallaxY = 0;
const startTime = performance.now();

// Pre-group geometry by color for batched drawing
const colorCount = P.colors.length;
let colorGroups;
if (P.type === 'lsystem') {{
  colorGroups = P.colors.map(() => ({{ edges: [], verts: [] }}));
  const edges = P.edges || [];
  for (let i = 0; i < edges.length; i++) colorGroups[i % colorCount].edges.push(edges[i]);
  const verts = P.vertices || [];
  for (let i = 0; i < verts.length; i++) colorGroups[i % colorCount].verts.push(verts[i]);
}} else {{
  colorGroups = P.colors.map(() => ([]));
  const pts = P.points || [];
  for (let i = 0; i < pts.length; i++) colorGroups[i % colorCount].push(pts[i]);
}}

function draw() {{
  const elapsed = (performance.now() - startTime) / 1000;

  // Clear
  ctx.fillStyle = P.backgroundColor || '{bg_color}';
  ctx.fillRect(0, 0, W, H);

  ctx.save();
  ctx.translate(W / 2, H / 2);

  // Mouse parallax (smooth lerp)
  parallaxX += (mx * 30 * (P.parallaxStrength || 0.4) - parallaxX) * 0.05;
  parallaxY += (my * 30 * (P.parallaxStrength || 0.4) - parallaxY) * 0.05;
  ctx.translate(parallaxX, parallaxY);

  // Breathing scale
  const breathe = 1.0 + Math.sin(elapsed * (P.animationSpeed || 1.0) * 0.4) * 0.04;
  ctx.scale(breathe, breathe);

  // Slow rotation
  angle += (P.animationSpeed || 1.0) * 0.002;
  ctx.rotate(angle);

  // Scale geometry to fit canvas (geometry is in [-1,1])
  const scale = Math.min(W, H) * 0.42;
  ctx.scale(scale, -scale);
  ctx.lineCap = 'round';

  if (P.type === 'lsystem') {{
    const lw = (P.lineWidth || 1) / scale;
    const ps = (P.particleSize || 0.008) * 2;

    // Glow pass: thicker + semi-transparent (no shadowBlur)
    ctx.globalAlpha = 0.15;
    ctx.lineWidth = lw * 4;
    for (let ci = 0; ci < colorCount; ci++) {{
      const grp = colorGroups[ci];
      if (!grp.edges.length) continue;
      ctx.strokeStyle = P.colors[ci];
      ctx.beginPath();
      for (const e of grp.edges) {{
        ctx.moveTo(e[0], e[1]);
        ctx.lineTo(e[2], e[3]);
      }}
      ctx.stroke();
    }}

    // Main pass: crisp lines
    ctx.globalAlpha = 1.0;
    ctx.lineWidth = lw;
    for (let ci = 0; ci < colorCount; ci++) {{
      const grp = colorGroups[ci];
      if (grp.edges.length) {{
        ctx.strokeStyle = P.colors[ci];
        ctx.beginPath();
        for (const e of grp.edges) {{
          ctx.moveTo(e[0], e[1]);
          ctx.lineTo(e[2], e[3]);
        }}
        ctx.stroke();
      }}
      // Vertex dots
      if (grp.verts.length) {{
        ctx.fillStyle = P.colors[ci];
        ctx.beginPath();
        for (const v of grp.verts) {{
          ctx.moveTo(v[0] + ps, v[1]);
          ctx.arc(v[0], v[1], ps, 0, Math.PI * 2);
        }}
        ctx.fill();
      }}
    }}

  }} else {{
    const ps = (P.particleSize || 0.012) * 1.5;

    // Glow pass
    ctx.globalAlpha = 0.12;
    for (let ci = 0; ci < colorCount; ci++) {{
      const pts = colorGroups[ci];
      if (!pts.length) continue;
      ctx.fillStyle = P.colors[ci];
      ctx.beginPath();
      for (const p of pts) {{
        ctx.moveTo(p[0] + ps * 2, p[1]);
        ctx.arc(p[0], p[1], ps * 2, 0, Math.PI * 2);
      }}
      ctx.fill();
    }}

    // Main pass
    ctx.globalAlpha = 0.85;
    for (let ci = 0; ci < colorCount; ci++) {{
      const pts = colorGroups[ci];
      if (!pts.length) continue;
      ctx.fillStyle = P.colors[ci];
      ctx.beginPath();
      for (const p of pts) {{
        ctx.moveTo(p[0] + ps, p[1]);
        ctx.arc(p[0], p[1], ps, 0, Math.PI * 2);
      }}
      ctx.fill();
    }}
    ctx.globalAlpha = 1.0;
  }}

  ctx.restore();
  requestAnimationFrame(draw);
}}
draw();
</script>
</body>
</html>"""
    return html
