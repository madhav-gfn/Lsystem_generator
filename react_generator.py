def extract_lsystem_geometry(segments, max_points=4000):
    """Extract and normalize L-system line segments for Three.js.

    Args:
        segments: List of (x1, y1, x2, y2) tuples from interpret_turtle().
        max_points: Maximum number of segments to keep.

    Returns:
        Dict with keys: type, vertices, edges, bounds, normalized_vertices.
    """
    if not segments:
        return {"type": "lsystem", "vertices": [], "edges": [],
                "bounds": {"min_x": 0, "max_x": 0, "min_y": 0, "max_y": 0},
                "normalized_vertices": []}

    # Sample if needed
    if len(segments) > max_points:
        step = len(segments) / max_points
        segments = [segments[int(i * step)] for i in range(max_points)]

    # Bounding box
    all_x = [v for s in segments for v in (s[0], s[2])]
    all_y = [v for s in segments for v in (s[1], s[3])]
    min_x, max_x = min(all_x), max(all_x)
    min_y, max_y = min(all_y), max(all_y)
    bounds = {"min_x": min_x, "max_x": max_x, "min_y": min_y, "max_y": max_y}

    # Normalize to [-1, 1]
    range_x = max_x - min_x if max_x != min_x else 1.0
    range_y = max_y - min_y if max_y != min_y else 1.0
    scale = max(range_x, range_y) / 2.0
    cx = (min_x + max_x) / 2.0
    cy = (min_y + max_y) / 2.0

    def norm(x, y):
        return [round((x - cx) / scale, 5), round((y - cy) / scale, 5)]

    vertices = []
    edges = []
    seen = set()
    for x1, y1, x2, y2 in segments:
        nx1, ny1 = norm(x1, y1)
        nx2, ny2 = norm(x2, y2)
        edges.append([nx1, ny1, nx2, ny2])
        for vx, vy in [(nx1, ny1), (nx2, ny2)]:
            key = (vx, vy)
            if key not in seen:
                seen.add(key)
                vertices.append([vx, vy])

    return {
        "type": "lsystem",
        "vertices": vertices,
        "edges": edges,
        "bounds": bounds,
        "normalized_vertices": vertices,
    }


def extract_ifs_geometry(points, max_points=6000):
    """Extract and normalize IFS point cloud for Three.js.

    Args:
        points: List of (x, y) tuples from the chaos game.
        max_points: Maximum number of points to keep.

    Returns:
        Dict with keys: type, points, density_hint.
    """
    if not points:
        return {"type": "ifs", "points": [], "density_hint": 0.0}

    total = len(points)

    # Sample if needed
    if total > max_points:
        step = total / max_points
        points = [points[int(i * step)] for i in range(max_points)]

    xs = [p[0] for p in points]
    ys = [p[1] for p in points]
    min_x, max_x = min(xs), max(xs)
    min_y, max_y = min(ys), max(ys)

    range_x = max_x - min_x if max_x != min_x else 1.0
    range_y = max_y - min_y if max_y != min_y else 1.0
    scale = max(range_x, range_y) / 2.0
    cx = (min_x + max_x) / 2.0
    cy = (min_y + max_y) / 2.0

    normalized = [
        [round((p[0] - cx) / scale, 5), round((p[1] - cy) / scale, 5)]
        for p in points
    ]

    return {
        "type": "ifs",
        "points": normalized,
        "density_hint": round(len(points) / max_points, 4),
    }


